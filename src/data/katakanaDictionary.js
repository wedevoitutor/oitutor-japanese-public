const lessonFileFor = (file) => `katakana/${file}`;
const idBaseFor = (kana) => `katakana-${kana}`;

const ENTRY_OVERRIDES = {
  アオ: {
    image: {
      alt: 'Blue color swatch',
      color: '#1d4ed8',
    },
    description: 'アオ is a katakana reading-practice form for the word blue.',
  },
  オーケー: {
    description: 'オーケー is the katakana loanword for OK.',
  },
  ナイス: {
    description: 'ナイス is a casual katakana word meaning nice.',
  },
  ユニーク: {
    description: 'ユニーク is a na-adjective used for something unique or distinctive.',
  },
};

const descriptionFor = (word, gloss) => {
  if (ENTRY_OVERRIDES[word]?.description) return ENTRY_OVERRIDES[word].description;
  return `${word} is a katakana reading-practice word meaning "${gloss}".`;
};

export const katakanaDictionary = KATAKANA_WORDS.map(([
  file,
  word,
  romaji,
  gloss,
  partOfSpeech = 'noun',
]) => {
  const override = ENTRY_OVERRIDES[word] || {};

  return {
    id: `${idBaseFor(word)}-v1`,
    word,
    kana: word,
    romaji,
    jlpt: 'N5',
    partOfSpeech,
    image: override.image,
    meanings: [
      {
        id: `${idBaseFor(word)}-meaning-1`,
        rank: 1,
        gloss,
        partOfSpeech,
        description: descriptionFor(word, gloss),
      },
    ],
    tags: ['katakana', 'reading-practice', lessonFileFor(file).replace('.json', '')],
    source: 'lesson',
    lessonFiles: [lessonFileFor(file)],
  };
});
