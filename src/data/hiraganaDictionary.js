export const hiraganaDictionary = HIRAGANA_WORDS.map(([
  file,
  word,
  kana,
  romaji,
  gloss,
  partOfSpeech,
  extraMeanings = [],
]) => {
  const override = ENTRY_OVERRIDES[kana] || {};

  const baseMeaning = {
    id: `${idBaseFor(kana)}-meaning-1`,
    rank: 1,
    gloss,
    partOfSpeech,
    description: override.description || gloss,
    example: override.example,
  };

  const additionalMeanings = extraMeanings.map((meaning, index) => ({
    id: `${idBaseFor(kana)}-meaning-${index + 2}`,
    rank: index + 2,
    description: meaning.description || meaning.gloss,
    ...meaning,
  }));

  return {
    id: `${idBaseFor(kana)}-v1`,
    word,
    kana,
    romaji,
    jlpt: 'N5',
    partOfSpeech,
    image: override.image,
    meanings: [baseMeaning, ...additionalMeanings],
    tags: ['hiragana', 'reading-practice', lessonFileFor(file).replace('.json', '')],
    source: 'lesson',
    lessonFiles: [lessonFileFor(file)],
  };
});
